package com.small.library.atg.gen;

import java.io.*;
import java.sql.*;
import java.util.Collection;
import java.util.HashMap;
import java.util.Iterator;
import java.util.Map;

import org.w3c.dom.*;
import org.xml.sax.InputSource;
import org.apache.xerces.parsers.DOMParser;

import com.small.library.atg.meta.*;
import com.small.library.generator.*;
import com.small.library.xml.XMLHelper;

/***************************************************************************************
*
*	Generator class for Dynamo SQL Repository Item Wrapper classes. Item Wrapper
*	classes are simple Java beans that encapsulate a <I>RepositoryItem</I> and
*	expose accessors and mutators of an entity for each property that closely
*	matches a property's type. This overcomes the overly simple <CODE>
*	getPropertyValue</CODE> and <CODE>setPropertyValue</CODE> methods of
*	<I>RepositoryItem</I> (<I>MutableRepositoryItem</I>) that do not enforce
*	strict type safety during compile.
*
*	<BR><BR>
*
*	The wrappers are generated by inspecting already created Repository Item
*	Descriptions, as opposed to using database meta data.
*
*	@author David Small
*	@version 1.0.0.0
*	@date 7/3/2002
*
***************************************************************************************/

public class SQLRepositoryItemWrapperByFile extends SQLRepositoryItemBase
{
	/******************************************************************************
	*
	*	Constants
	*
	*****************************************************************************/

	/******************************************************************************
	*
	*	Static members
	*
	*****************************************************************************/

	/******************************************************************************
	*
	*	Constructors
	*
	*****************************************************************************/

	/** Constructor - constructs an empty object. */
	public SQLRepositoryItemWrapperByFile() { super(); }

	/** Constructor - constructs a populated object.
		@param pWriter The output stream.
		@param strAuthor Name of the author.
		@param pDocuemnt XML DOM document that contains the parsed
			Repository Item Descriptor.
	*/
	public SQLRepositoryItemWrapperByFile(PrintWriter pWriter,
		String strAuthor, Document pDocument)
			throws DynamoMetaException
	{
		super(pWriter, strAuthor, pDocument);
	}

	/** Constructor - constructs a populated object.
		@param pWriter The output stream.
		@param strAuthor Name of the author.
		@param pDocuemnt XML DOM document that contains the parsed
			Repository Item Descriptor.
		@param strPackageName Package name of the wrapper class.
	*/
	public SQLRepositoryItemWrapperByFile(PrintWriter pWriter,
		String strAuthor, Document pDocument, String strPackageName)
			throws DynamoMetaException
	{
		super(pWriter, strAuthor, pDocument, strPackageName);
	}

	/******************************************************************************
	*
	*	Required methods: Base
	*
	*****************************************************************************/

	/** Action method - generates the SQL Repository Item Descriptor. */
	public void generate() throws GeneratorException, IOException
	{
		// Output the wrapper class.
		writeHeader();
		writeClassDeclaration();
		writeInstanceCreationHelpers();
		writeConstructors();

		writeAccessors();
		writeMutators();

		writeFooter();
	}

	/******************************************************************************
	*
	*	Helper methods
	*
	*****************************************************************************/

	/******************************************************************************
	*
	*	Output methods
	*
	*****************************************************************************/

	/** Output method - writes the file header. */
	private void writeHeader() throws IOException
	{
		String strPackageName = getPackageName();

		if (null != strPackageName)
		{
			writeLine("package " + strPackageName + ";");
			writeLine();
		}

		// Must include "java.util.*" becuase of Date, Set, List, and Map.

		writeLine("import java.sql.Timestamp;");
		writeLine("import java.util.*;");
		writeLine();
		writeLine("import atg.repository.*;");
		writeLine();
		writeLine("import " + getFrameworkPackage() + ".*;");
		writeLine();
		writeLine("/**********************************************************************************");
		writeLine("*");
		writeLine("*\tBean class that represents the " + getName() + " repository item. Acts ");
		writeLine("*\tas a wrapper for the Repository Item that represents the " + getName());
		writeLine("*\tentity.");
		writeLine("*");
		writeLine("*\t@author " + getAuthor());
		writeLine("*\t@version 1.0.0.0");
		writeLine("*\t@date " + getDateString());
		writeLine("*");
		writeLine("**********************************************************************************/");
	}

	/** Output method - writes the class declaration. */
	private void writeClassDeclaration() throws IOException
	{
		writeLine();
		writeLine("public class " + getClassName() + " extends MutableRepositoryBean");
		writeLine("{");
	}

	/** Output method - writes the instance creation helpers. */
	private void writeInstanceCreationHelpers() throws IOException
	{
		String strName = getName();
		String strClassName = getClassName();

		writeLine("\t/**************************************************************************");
		writeLine("\t*");
		writeLine("\t*\tHelper methods - Instance creation methods");
		writeLine("\t*");
		writeLine("\t**************************************************************************/");
		writeLine();
		writeLine("\t/** Helper method - constructs an array of " + strName + " repository beans");
		writeLine("\t    from an array of Mutable Repository Items.");
		writeLine("\t\t@param repositoryItems Array of Mutable Repository Items.");
		writeLine("\t\t@param pClassType A <I>Class</I> object that represents the");
		writeLine("\t\t\tspecific entity repository bean to construct.");
		writeLine("\t*/");
		writeLine("\tpublic static " + strClassName + "[] createRepositoryBeans(");
		writeLine("\t\tMutableRepositoryItem[] repositoryItems)");
		writeLine("\t\t\tthrows RepositoryBeanCreationException");
		writeLine("\t{");
		writeLine("\t\treturn (" + strClassName + "[]) createRepositoryBeans(repositoryItems,");
		writeLine("\t\t\t" + strClassName + ".class);");
		writeLine("\t}");
		writeLine();
		writeLine("\t/** Helper method - constructs an array of " + strName + " repository beans");
		writeLine("\t    from an array of Repository Items.");
		writeLine("\t\t@param repositoryItems Array of Repository Items.");
		writeLine("\t\t@param pClassType A <I>Class</I> object that represents the");
		writeLine("\t\t\tspecific entity repository bean to construct.");
		writeLine("\t*/");
		writeLine("\tpublic static " + strClassName + "[] createRepositoryBeans(");
		writeLine("\t\tRepositoryItem[] repositoryItems)");
		writeLine("\t\t\tthrows RepositoryBeanCreationException");
		writeLine("\t{");
		writeLine("\t\treturn createRepositoryBeans((MutableRepositoryItem[]) repositoryItems);");
		writeLine("\t}");
	}

	/** Output method - writes the constructors. */
	private void writeConstructors() throws IOException
	{
		String strName = getName();
		String strClassName = getClassName();

		writeLine();
		writeLine("\t/**************************************************************************");
		writeLine("\t*");
		writeLine("\t*\tConstructors");
		writeLine("\t*");
		writeLine("\t**************************************************************************/");
		writeLine();
		writeLine("\t/** Constructor - constructs a populated object.");
		writeLine("\t\t@param repositoryItem A Repository Item object that contains");
		writeLine("\t\t\t" + strName + " record values.");
		writeLine("\t*/");
		writeLine("\tpublic " + strClassName + "(RepositoryItem repositoryItem)");
		writeLine("\t{");
		writeLine("\t\tthis((MutableRepositoryItem) repositoryItem);");
		writeLine("\t}");
		writeLine();
		writeLine("\t/** Constructor - constructs a populated object.");
		writeLine("\t\t@param repositoryItem A Mutable Repository Item object that contains");
		writeLine("\t\t\t" + strName + " record values.");
		writeLine("\t*/");
		writeLine("\tpublic " + strClassName + "(MutableRepositoryItem repositoryItem)");
		writeLine("\t{");
		writeLine("\t\tsuper(repositoryItem);");
		writeLine("\t}");
	}

	/** Output method - writes the bean accessors. */
	private void writeAccessors() throws IOException
	{
		Collection properties = getDefaultItemDescriptor().getProperties();
		Iterator itProperties = properties.iterator();

		writeLine();
		writeLine("\t/**************************************************************************");
		writeLine("\t*");
		writeLine("\t*\tAccessor methods");
		writeLine("\t*");
		writeLine("\t**************************************************************************/");

		while (itProperties.hasNext())
		{
			Property property = (Property) itProperties.next();
			String strObjectName = property.getName();
			String strPropertyMethodSuffix = getPropertyMethodSuffix(property);
			String strDataType = getJavaType(property);

			writeLine();
			writeLine("\t/** Accessor method - gets the " + strObjectName + " property. */");
			writeLine("\tpublic " + strDataType + " get" + strObjectName +
				"() { return getProperty" + strPropertyMethodSuffix +
				"(\"" + strObjectName + "\"); }");
		}
	}

	/** Output method - writes the bean mutators. */
	private void writeMutators() throws IOException
	{
		Collection properties = getDefaultItemDescriptor().getProperties();
		Iterator itProperties = properties.iterator();

		writeLine();
		writeLine("\t/**************************************************************************");
		writeLine("\t*");
		writeLine("\t*\tMutator methods");
		writeLine("\t*");
		writeLine("\t**************************************************************************/");

		while (itProperties.hasNext())
		{
			Property property = (Property) itProperties.next();
			String strObjectName = property.getName();
			String strDataType = getJavaType(property);

			writeLine();
			writeLine("\t/** Mutator method - sets the " + strObjectName + " property. */");
			writeLine("\tpublic void set" + strObjectName + "(" +
				strDataType + " newValue) { setPropertyValue(\"" + strObjectName +
				"\", newValue); }");
		}
	}

	/** Output method - write the class footer. */
	private void writeFooter() throws IOException
	{
		writeLine("}");
	}

	/******************************************************************************
	*
	*	Accessor methods
	*
	*****************************************************************************/

	/** Accessor method - gets the object name of the wrapper class.
	*/
	public String getClassName() { return m_strClassName; }

	/******************************************************************************
	*
	*	Mutator methods
	*
	*****************************************************************************/

	/** Mutator method - sets the XML DOM document that contains the parsed
	    Repository Item Descriptor.
	*/
	public void setDocument(Document document)
		throws DynamoMetaException, NullPointerException
	{
		super.setDocument(document);

		m_strClassName = getName() + "RepositoryBean";
	}

	/******************************************************************************
	*
	*	Member variables
	*
	*****************************************************************************/

	/** Member variable - reference to the object name of the wrapper class. */
	private String m_strClassName = null;

	/******************************************************************************
	*
	*	Class entry point
	*
	*****************************************************************************/

	/** Command line entry point.
		@param strArg1 Output directory.
		@param strArg2 Input directory of the Repository Item Descriptors.
		@param strArg3 author of the generated classes. Will use the
			"user.name" system property value if not supplied.
		@param strArg4 package name of the wrapper class.
	*/
	public static void main(String strArgs[])
	{
		try
		{
			// Have enough arguments been supplied?
			if (2 > strArgs.length)
				throw new IllegalArgumentException("Please supply at least 2 arguments.");

			// Local variables
			File fileOutputDir = extractOutputDirectory(strArgs, 0);
			File fileInputDir = extractDirectory(strArgs, 1, "input");
			String strAuthor = extractAuthor(strArgs, 2);
			String strPackageName = extractArgument(strArgs, 3, null);

			// Get the list of files.
			FileFilter fileFilter = new com.small.library.util.FileFilterByExtension("xml");
			File[] fileItemDescriptors =
				fileInputDir.listFiles(fileFilter);

			// Create the SQL Repository Item Descriptor generator.
			SQLRepositoryItemWrapperByFile generator =
				new SQLRepositoryItemWrapperByFile((PrintWriter) null, strAuthor,
				null, strPackageName);

			// Generate repository resource.
			generateRepositoryResource(generator, fileItemDescriptors, fileOutputDir);
		}

		catch (IllegalArgumentException pEx)
		{
			String strMessage = pEx.getMessage();

			if (null != strMessage)
			{
				System.out.println(strMessage);
				System.out.println();
			}

			System.out.println("Usage: java " + SQLRepositoryItemWrapperByFile.class.getName() + " Output directory");
			System.out.println("\tInput directory of the Repository Item Descriptors");
			System.out.println("\t[Author]");
			System.out.println("\t[Package Name]");
		}

		catch (Exception pEx)
		{
			pEx.printStackTrace();
		}
	}
}
